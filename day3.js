// Temperature Converter Practice
// Let's convert 25 degrees Celsius to Fahrenheit
// Formula: (Celsius * 9/5) + 32 = Fahrenheit

const celsius = 25;
const fahrenheit = (celsius * 9/5) + 32

console.log(celsius + " degrees Celsius equals " + fahrenheit + " degrees Fahrenheit");
// Output: 25 degrees Celsius equals 77 degrees Fahrenheit

// Let's break it down:
// Step 1: celsius * 9 = 25 * 9 = 225
// Step 2: 225 / 5 = 45
// Step 3: 45 + 32 = 77